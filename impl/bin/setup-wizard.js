#!/usr/bin/env node
/**
 * OpenClaw Setup Wizard
 * 
 * 借鉴 DeerFlow 2.0 的交互式配置生成器
 * 2分钟引导用户完成配置
 * 
 * 来源: https://github.com/bytedance/deer-flow
 * 参考: Makefile `make setup` 命令
 */

const fs = require('fs');
const path = require('path');
const readline = require('readline');

const CONFIG_FILE = path.join(__dirname, '..', '..', 'gateway-config.yaml');
const ENV_FILE = path.join(__dirname, '..', '..', '.env');

const rl = readline.createInterface({
  input: process.stdin,
  output: process.stdout
});

/**
 * 问答流程
 */
const questions = [
  {
    key: 'llm_provider',
    question: 'Which LLM provider do you want to use?',
    options: ['bailian', 'openai', 'anthropic'],
    default: 'bailian'
  },
  {
    key: 'default_model',
    question: 'Which model do you want to use?',
    options: {
      bailian: ['glm-5', 'qwen-max'],
      openai: ['gpt-4o', 'gpt-4'],
      anthropic: ['claude-3-5-sonnet-20241022', 'claude-3-opus']
    },
    default: 'glm-5'
  },
  {
    key: 'api_key',
    question: 'Enter your API key (or leave empty to use env var):',
    default: ''
  },
  {
    key: 'channel',
    question: 'Which IM channel do you want to configure?',
    options: ['feishu', 'telegram', 'discord', 'none'],
    default: 'feishu'
  },
  {
    key: 'heartbeat_enabled',
    question: 'Enable heartbeat auto-polling?',
    options: ['yes', 'no'],
    default: 'yes'
  },
  {
    key: 'heartbeat_interval',
    question: 'Heartbeat polling interval (minutes)?',
    default: '30'
  }
];

/**
 * 生成 gateway-config.yaml
 */
function generateConfig(answers) {
  const { llm_provider, default_model, channel, heartbeat_enabled, heartbeat_interval } = answers;

  const config = `# OpenClaw Gateway Configuration
# Generated by setup-wizard at ${new Date().toISOString()}

agents:
  defaults:
    model: ${llm_provider}/${default_model}
    heartbeat:
      enabled: ${heartbeat_enabled === 'yes'}
      interval: ${parseInt(heartbeat_interval) * 60000}

llm:
  providers:
    - name: ${llm_provider}
      type: ${llm_provider}
      models:
        - name: ${default_model}
          api_key: $\${${llm_provider.toUpperCase()}_API_KEY}

channels:
  ${channel === 'feishu' ? `- name: feishu
    enabled: true
    app_id: $\${FEISHU_APP_ID}
    app_secret: $\${FEISHU_APP_SECRET}` : channel === 'telegram' ? `- name: telegram
    enabled: true
    bot_token: $\${TELEGRAM_BOT_TOKEN}` : channel === 'discord' ? `- name: discord
    enabled: true
    bot_token: $\${DISCORD_BOT_TOKEN}` : '# No channels configured'}
`;

  return config;
}

/**
 * 生成 .env 模板
 */
function generateEnvTemplate(answers) {
  const { llm_provider, api_key, channel } = answers;

  let env = `# OpenClaw Environment Variables
# Generated by setup-wizard at ${new Date().toISOString()}

# LLM Provider
${llm_provider.toUpperCase()}_API_KEY=${api_key || 'your_api_key_here'}

`;

  if (channel === 'feishu') {
    env += `# Feishu Channel
FEISHU_APP_ID=your_app_id_here
FEISHU_APP_SECRET=your_app_secret_here
`;
  } else if (channel === 'telegram') {
    env += `# Telegram Channel
TELEGRAM_BOT_TOKEN=your_bot_token_here
`;
  } else if (channel === 'discord') {
    env += `# Discord Channel
DISCORD_BOT_TOKEN=your_bot_token_here
`;
  }

  env += `
# MCP OAuth (Optional)
MCP_OAUTH_CLIENT_ID=
MCP_OAUTH_CLIENT_SECRET=
`;

  return env;
}

/**
 * 提问并获取答案
 */
async function askQuestion(question) {
  return new Promise((resolve) => {
    const { key, question: q, options, default: defaultValue } = question;
    
    let prompt = q;
    if (options) {
      if (Array.isArray(options)) {
        prompt += `\nOptions: ${options.join(', ')}`;
      }
    }
    prompt += `\nDefault: ${defaultValue}\n> `;
    
    rl.question(prompt, (answer) => {
      resolve({
        key,
        value: answer.trim() || defaultValue
      });
    });
  });
}

/**
 * 主流程
 */
async function runWizard() {
  console.log('\n🦌 OpenClaw Setup Wizard\n');
  console.log('This wizard will guide you through creating gateway-config.yaml and .env\n');
  console.log('Estimated time: 2 minutes\n');

  const answers = {};

  for (const question of questions) {
    const { key, value } = await askQuestion(question);
    answers[key] = value;
  }

  rl.close();

  console.log('\n📝 Generating configuration files...\n');

  // 生成配置
  const config = generateConfig(answers);
  const env = generateEnvTemplate(answers);

  // 写入文件
  fs.writeFileSync(CONFIG_FILE, config);
  console.log(`✅ Created: ${CONFIG_FILE}`);

  if (!fs.existsSync(ENV_FILE)) {
    fs.writeFileSync(ENV_FILE, env);
    console.log(`✅ Created: ${ENV_FILE}`);
  } else {
    console.log(`⚠️  ${ENV_FILE} already exists, skipping (backup and recreate if needed)`);
  }

  console.log('\n🎉 Setup complete!\n');
  console.log('Next steps:\n');
  console.log('1. Edit .env and fill in your actual API keys');
  console.log('2. Run: openclaw gateway start');
  console.log('3. Run: openclaw gateway status (to verify)');
  console.log('\n');

  if (answers.channel === 'feishu') {
    console.log('For Feishu setup:');
    console.log('- Create a Feishu app: https://open.feishu.cn/app');
    console.log('- Get app_id and app_secret');
    console.log('- Add them to .env');
    console.log('\n');
  }
}

// 命令处理
const args = process.argv.slice(2);
const command = args[0];

if (command === 'run') {
  runWizard().catch(err => {
    console.error('Setup wizard failed:', err.message);
    process.exit(1);
  });
} else {
  console.log('Usage: setup-wizard.js run');
  console.log('');
  console.log('This wizard will:');
  console.log('  1. Ask LLM provider + model');
  console.log('  2. Ask API key');
  console.log('  3. Ask IM channel');
  console.log('  4. Ask heartbeat settings');
  console.log('  5. Generate gateway-config.yaml + .env');
  console.log('');
  console.log('Run `node setup-wizard.js run` to start.');
}